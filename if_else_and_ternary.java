public class if_else_and_ternary {
    public static void main(String[] args){
        //if-else code blocks

        boolean condition = true;
        if(condition)
        {
            System.out.println("The condition is true");
        }
        else
        {
            System.out.println("The condition is false");
        }

        //ternary operators
        System.out.println((5 > 3) ? "5 is greater than 3" : "5 is less than 3");
    }
}
